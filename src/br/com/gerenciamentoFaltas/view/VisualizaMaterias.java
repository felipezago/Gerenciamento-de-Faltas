    public Materias retornaObjeto(){
        Materias mat= new Materias();
        mat.setNome_materia(txMateria.getText().toUpperCase());
        return mat;
    }
    
    public void atualizaTabela(){
        try {
            lista= MateriasDAO.selectDB(retornaObjeto());
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        
            DefaultTableModel model= (DefaultTableModel) tbMat.getModel();
            model.setNumRows(0);
            for(Materias mat: lista){
                model.addRow(new Object[]{
                   mat.getNome_materia(),
                   mat.getQtd_Max_faltas()
                });
                
            }      
    }
    

