    private void btnAltMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAltMouseReleased
        try{
            Materias matSelec= lista.get(tbMat.getSelectedRow());
            new CadastroMaterias(matSelec).setVisible(true);           
        }catch(ArrayIndexOutOfBoundsException ex){
            JOptionPane.showMessageDialog(null, "FAVOR SELECIONAR UMA LINHA DA TABELA PARA EXCLUIR/MODIFICAR", "Aviso", JOptionPane.WARNING_MESSAGE);
        }

        
    }//GEN-LAST:event_btnAltMouseReleased

    private void btnExcluirMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExcluirMouseReleased
        try{
            Materias materiaSelec= lista.get(tbMat.getSelectedRow());

            int opc = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir a matéria: "+materiaSelec.getNome_materia()+"?");

            if(opc == JOptionPane.YES_OPTION){
                try {
                    MateriasDAO.deleteDB(materiaSelec);
                } catch (SQLException ex) {
                    ex.printStackTrace();
                } 
                atualizaTabela();
            }
        }catch(ArrayIndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(null, "FAVOR SELECIONAR UMA LINHA DA TABELA PARA EXCLUIR/MODIFICAR", "Aviso", JOptionPane.WARNING_MESSAGE);

        }
    
    }//GEN-LAST:event_btnExcluirMouseReleased

