    private void btnSalvarMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalvarMouseReleased
        String info = txQt.getText();
        try {
            if (Integer.parseInt(info) < 100 && Integer.parseInt(info) > 0) {
                if (validaCampos()) {
                    if (salvar) {
                        MateriasDAO.insertDB(retornaObjeto());
                        txMateria.setText("");
                        txQt.setText("");
                        JOptionPane.showMessageDialog(null, "Dados inseridos com Sucesso");
                    } else {
                        MateriasDAO.updateDB(retornaObjeto());
                        txMateria.setText("");
                        txQt.setText("");
                        this.dispose();
                    }
                }

            } else {
                JOptionPane.showMessageDialog(null, "Quantidade inválida");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Por favor, digite um caractér valido.");
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSalvarMouseReleased

