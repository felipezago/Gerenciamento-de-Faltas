    public Materias retornaObjeto() {
        Materias mat = new Materias();
        mat.setNome_materia(txMateria.getText());
        mat.setQtd_Max_faltas(Integer.parseInt(txQt.getText()));
        mat.setId_materias(id);
        return mat;
    }

    public void PreencheCampos(Materias materias) {
        txMateria.setText(materias.getNome_materia());
        txQt.setText(""+ materias.getQtd_Max_faltas() +"");
        System.out.println(materias.getQtd_Max_faltas());

    }

    public boolean validaCampos() {
        boolean check = true;

        txMateria.setText(txMateria.getText().trim());

        if (txMateria.getText().equals("")) {

            //ele ira validar se o tx esta preenchido
            check = false;

            //deixa a borda vermelha se nao preenchido nada
            txMateria.setBorder(BorderFactory.createLineBorder(Color.red));
            JOptionPane.showMessageDialog(null, "Preencher Campos Obrigatorios");
        } else {

            txMateria.setBorder(MetalBorders.getTextFieldBorder());
        }

        if (txQt.getText().equals("")) {

            //ele ira validar se o tx esta preenchido
            check = false;

            //deixa a borda vermelha se nao preenchido nada
            txQt.setBorder(BorderFactory.createLineBorder(Color.red));
            JOptionPane.showMessageDialog(null, "Preencher Campos Obrigatorios");
        } else {

            txQt.setBorder(MetalBorders.getTextFieldBorder());
        }
        return check;
    }

